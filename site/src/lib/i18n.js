// ── hook ──────────────────────────────────────────────────────────────────────
// Returns a t() function bound to the current language
export function useT(lang) {
  return (key, ...args) => {
    const val = translations[lang]?.[key] ?? translations['ko'][key] ?? key
    return typeof val === 'function' ? val(...args) : val
  }
}
